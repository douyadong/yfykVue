function IndexController() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    继承于Controller基类
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this) ;
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     实例化tab页面
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.initTabs() ;
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     加微信
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.addWeixin() ;
} ;
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 实例化tab页面
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
IndexController.prototype.initTabs = function() {
    $("#house-section .wkzf-tabs").tabs({            
        duration : 200 ,    
        fixedWhenScroll : false
    }) ;
} ;
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 加微信
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
IndexController.prototype.addWeixin = function() {
    $("#drift-tools .weixin").click(function(){
        $("#qrcode-suit").show() ;
    }) ;
} ;
/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 类的初始化
-----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new IndexController() ;
}) ;